export function getWordColor(wordIndex?: number) {
  switch (wordIndex) {
    case 0:
      return "#FFB300";
    case 1:
      return "#1BA9F5";
    case 2:
      return "#D726FF";
    case 3:
      return "#9AF50F";
    case 4:
      return "#E92E2E";
    case 5:
      return "#FF8A00";
    case 6:
      return "#FFA733";
    case 7:
      return "#E69900";
    case 8:
      return "#D68A00";
    default:
      return "#FFB300";
  }
}
